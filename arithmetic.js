function PerformCalc()
{
	return (2+3)
}
document.write("The sum of 2 + 3 = " + PerformCalc() + ".\n")

function PerformSub()
{
	return (10-3)
}
document.write("The difference of 10 - 3 = " + PerformSub() + ".\n")

function PerformMulti()
{
	return(5*4)
}
document.write("The product of 5 x 4 = " + PerformMulti() + ".\n")

function PerformDiv()
{
	return(64/4)
}
document.write("The quotient of 64 / 3 = " + PerformDiv() + ".\n")
